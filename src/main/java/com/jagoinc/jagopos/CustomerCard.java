    private void customerDialogSaveAndCloseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_customerDialogSaveAndCloseButtonActionPerformed
        if(_updateFlag){
            try {
                populateandUpdateResultSet();
            } catch (SQLException ex) {
                Logger.getLogger(ProductCard.class.getName()).log(Level.SEVERE, null, ex);
                System.exit(80085);
            }
            this.dispose();
            return;
        }
        Customer customer = null;
        try {
            customer = new Customer();
        } catch (SQLException ex) {
            Logger.getLogger(CustomerCard.class.getName()).log(Level.SEVERE, null, ex);
        }
        String escapedDescription = "";
        
        if(customerNameTextField.getText().contains("'")){
            escapedDescription = customerNameTextField.getText().replace("'", "\\'");
        }else{
            escapedDescription = customerNameTextField.getText();
        }
        if(customerEmailAddressTextField.getText().length() > 0){
            if(customerEmailAddressTextField.getText().contains("@") && customerEmailAddressTextField.getText().contains(".")){
                customer.setEmail(customerEmailAddressTextField.getText());
            }else{
                JOptionPane.showMessageDialog(this, "Please make sure your e-mail is valid.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        customer.setName(escapedDescription);
        customer.setPostalCode(customerPostalCodeTextField.getText());
        customer.setPhone(customerPhoneNumberTextField.getText());
        
        try {
            if(customer.saveCustomer()){
                this.dispose();
            }
        } catch (SQLException ex) {
            System.out.println("Error was caught:");
            System.out.println(ex.getMessage());
        }
        
    }//GEN-LAST:event_customerDialogSaveAndCloseButtonActionPerformed

