    public void populateCard(ResultSet customer) throws SQLException {
        customerIDTextLabel.setText(customer.getString(1));
        customerPostalCodeTextField.setText(customer.getString(2));
        customerNameTextField.setText(customer.getString(3));
        customerPhoneNumberTextField.setText(customer.getString(4));
        customerEmailAddressTextField.setText(customer.getString(5));
        _updateSet = customer;
    }
    private void populateandUpdateResultSet() throws SQLException{
        
        _updateSet.updateString(2, customerPostalCodeTextField.getText());
        _updateSet.updateString(3, customerNameTextField.getText());
        _updateSet.updateString(4, customerPhoneNumberTextField.getText());
        _updateSet.updateString(5, customerEmailAddressTextField.getText());
        _updateSet.updateRow();
    }
    
