    private void saveProductButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveProductButtonActionPerformed
        
        if(_updateFlag){
            try {
                populateandUpdateResultSet();
            } catch (SQLException ex) {
                Logger.getLogger(ProductCard.class.getName()).log(Level.SEVERE, null, ex);
                System.exit(80085);
            }
            this.dispose();
            return;
        }
        //check for UPC uniqueness and presence here eventually
        
        long UPC = 0;
        int inventory = 0;
        double cost = 0, salePrice = 0;
        String escapedDescription = "";
        
        if(productUPCTextField.getText().length() == 0 ){
            JOptionPane.showMessageDialog(this, "A UPC is required. Please enter UPC.", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            UPC = Long.parseLong(productUPCTextField.getText());
        }
        if(inventoryTextField.getText().length() == 0 ){
        }else{
            inventory = Integer.parseInt(inventoryTextField.getText());
        }
        if(costTextField.getText().length() == 0 ){
        }else{
            cost = Double.parseDouble(costTextField.getText());
        }
        if(salePriceTextField.getText().length() == 0 ){
        }else{
            salePrice = Double.parseDouble(salePriceTextField.getText());
        }
        
        if(productNameTextField.getText().contains("'")){
            escapedDescription = productNameTextField.getText().replace("'", "\\'");
        }else{
            escapedDescription = productNameTextField.getText();
        }
        
        _product.setUPC(UPC);
        _product.setDescription(escapedDescription);
        _product.setInventory(inventory);
        _product.setCost(cost);
        _product.setPrice(salePrice);
        
        try {
            if(_product.saveProduct()){
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(this, "That Product UPC is already in use. Please choose another.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            System.out.println("Error was caught:");
            System.out.println(ex.getMessage());
        }
        
    }//GEN-LAST:event_saveProductButtonActionPerformed

