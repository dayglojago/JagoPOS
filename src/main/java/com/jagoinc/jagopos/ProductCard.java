    public void populateCard(ResultSet product) throws SQLException{
        
        productUPCTextField.setText(product.getString(1));
        productNameTextField.setText(product.getString(2));
        costTextField.setText(product.getString(3));
        salePriceTextField.setText(product.getString(4));
        inventoryTextField.setText(product.getString(5));
        _updateSet = product;
    }
    
    private void populateandUpdateResultSet() throws SQLException{
        
        _updateSet.updateLong(1, Long.parseLong(productUPCTextField.getText()));
        _updateSet.updateString(2, productNameTextField.getText());
        _updateSet.updateString(3, costTextField.getText());
        _updateSet.updateString(4, salePriceTextField.getText());
        _updateSet.updateString(5, inventoryTextField.getText());
        _updateSet.updateRow();
    }
    
